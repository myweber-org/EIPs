class DataCleaner:
    def __init__(self, df):
        self.df = df.copy()
        self.original_shape = df.shape
        
    def remove_outliers_iqr(self, columns=None, threshold=1.5):
        if columns is None:
            columns = self.df.select_dtypes(include=[np.number]).columns
            
        df_clean = self.df.copy()
        for col in columns:
            if col in df_clean.columns:
                Q1 = df_clean[col].quantile(0.25)
                Q3 = df_clean[col].quantile(0.75)
                IQR = Q3 - Q1
                lower_bound = Q1 - threshold * IQR
                upper_bound = Q3 + threshold * IQR
                df_clean = df_clean[(df_clean[col] >= lower_bound) & (df_clean[col] <= upper_bound)]
        
        removed_count = len(self.df) - len(df_clean)
        self.df = df_clean
        return removed_count
    
    def impute_missing(self, strategy='median', columns=None):
        if columns is None:
            columns = self.df.select_dtypes(include=[np.number]).columns
            
        df_imputed = self.df.copy()
        for col in columns:
            if col in df_imputed.columns and df_imputed[col].isnull().any():
                if strategy == 'mean':
                    fill_value = df_imputed[col].mean()
                elif strategy == 'median':
                    fill_value = df_imputed[col].median()
                elif strategy == 'mode':
                    fill_value = df_imputed[col].mode()[0]
                else:
                    fill_value = 0
                
                df_imputed[col] = df_imputed[col].fillna(fill_value)
        
        self.df = df_imputed
        return self.df.isnull().sum().sum()
    
    def normalize_data(self, columns=None, method='zscore'):
        if columns is None:
            columns = self.df.select_dtypes(include=[np.number]).columns
            
        df_normalized = self.df.copy()
        for col in columns:
            if col in df_normalized.columns:
                if method == 'zscore':
                    df_normalized[col] = stats.zscore(df_normalized[col])
                elif method == 'minmax':
                    min_val = df_normalized[col].min()
                    max_val = df_normalized[col].max()
                    if max_val != min_val:
                        df_normalized[col] = (df_normalized[col] - min_val) / (max_val - min_val)
        
        self.df = df_normalized
        return self.df
    
    def get_cleaned_data(self):
        return self.df
    
    def get_cleaning_stats(self):
        final_shape = self.df.shape
        return {
            'original_rows': self.original_shape[0],
            'final_rows': final_shape[0],
            'rows_removed': self.original_shape[0] - final_shape[0],
            'original_columns': self.original_shape[1],
            'final_columns': final_shape[1]
        }

