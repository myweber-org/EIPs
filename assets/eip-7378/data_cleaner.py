    """
    Validate data structure and values
    """
    missing_columns = [col for col in required_columns if col not in data.columns]
    if missing_columns:
        raise ValueError(f"Missing required columns: {missing_columns}")
    
    if numeric_ranges:
        for column, (min_val, max_val) in numeric_ranges.items():
            if column in data.columns:
                invalid_values = data[(data[column] < min_val) | (data[column] > max_val)]
                if not invalid_values.empty:
                    print(f"Warning: Column '{column}' contains values outside range [{min_val}, {max_val}]")
    
