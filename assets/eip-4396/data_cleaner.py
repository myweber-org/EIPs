def clean_dataframe(df, drop_duplicates=True, fillna_method=None, fillna_value=None):
    """
    Clean a pandas DataFrame by handling missing values and optionally removing duplicates.
    
    Args:
        df (pd.DataFrame): Input DataFrame to clean.
        drop_duplicates (bool): If True, remove duplicate rows.
        fillna_method (str): Method to fill missing values ('ffill', 'bfill', or None).
        fillna_value: Scalar value to fill missing values if fillna_method is None.
    
    Returns:
        pd.DataFrame: Cleaned DataFrame.
    """
    cleaned_df = df.copy()
    
    # Handle missing values
    if fillna_method in ['ffill', 'bfill']:
        cleaned_df = cleaned_df.fillna(method=fillna_method)
    elif fillna_value is not None:
        cleaned_df = cleaned_df.fillna(fillna_value)
    
    # Remove duplicates
    if drop_duplicates:
        cleaned_df = cleaned_df.drop_duplicates().reset_index(drop=True)
    
    return cleaned_df

