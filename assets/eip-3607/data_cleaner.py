import pandas as pd
import re

def clean_dataframe(df, column_names=None):
    """
    Clean a pandas DataFrame by removing duplicates and normalizing string columns.
    """
    df_clean = df.copy()
    
    # Remove duplicate rows
    df_clean = df_clean.drop_duplicates()
    
    # If specific columns are provided, clean only those; otherwise, clean all object columns
    if column_names is None:
        column_names = df_clean.select_dtypes(include=['object']).columns
    
    for col in column_names:
        if col in df_clean.columns and df_clean[col].dtype == 'object':
            df_clean[col] = df_clean[col].apply(lambda x: normalize_string(x) if pd.notnull(x) else x)
    
    return df_clean

def normalize_string(s):
    """
    Normalize a string: convert to lowercase, remove extra whitespace, and strip.
    """
    if not isinstance(s, str):
        return s
    s = s.lower()
    s = re.sub(r'\s+', ' ', s)
    return s.strip()
