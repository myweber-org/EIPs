import pandas as pd
import numpy as np

def clean_dataset(df, drop_duplicates=True, fill_missing=True, fill_strategy='mean'):
    """
    Clean a pandas DataFrame by removing duplicates and handling missing values.
    
    Parameters:
    df (pd.DataFrame): Input DataFrame to clean.
    drop_duplicates (bool): Whether to drop duplicate rows.
    fill_missing (bool): Whether to fill missing values.
    fill_strategy (str): Strategy for filling missing values ('mean', 'median', 'mode', or 'constant').
    
    Returns:
    pd.DataFrame: Cleaned DataFrame.
    """
    cleaned_df = df.copy()
    
    if drop_duplicates:
        initial_rows = cleaned_df.shape[0]
        cleaned_df = cleaned_df.drop_duplicates()
        removed = initial_rows - cleaned_df.shape[0]
        print(f"Removed {removed} duplicate rows.")
    
    if fill_missing and cleaned_df.isnull().sum().any():
        numeric_cols = cleaned_df.select_dtypes(include=[np.number]).columns
        categorical_cols = cleaned_df.select_dtypes(exclude=[np.number]).columns
        
        for col in cleaned_df.columns:
            if cleaned_df[col].isnull().sum() > 0:
                if col in numeric_cols:
                    if fill_strategy == 'mean':
                        fill_value = cleaned_df[col].mean()
                    elif fill_strategy == 'median':
                        fill_value = cleaned_df[col].median()
                    elif fill_strategy == 'constant':
                        fill_value = 0
                    else:
                        fill_value = cleaned_df[col].mean()
                    cleaned_df[col].fillna(fill_value, inplace=True)
                elif col in categorical_cols:
                    if fill_strategy == 'mode':
                        fill_value = cleaned_df[col].mode()[0] if not cleaned_df[col].mode().empty else 'Unknown'
                    elif fill_strategy == 'constant':
                        fill_value = 'Unknown'
                    else:
                        fill_value = cleaned_df[col].mode()[0] if not cleaned_df[col].mode().empty else 'Unknown'
                    cleaned_df[col].fillna(fill_value, inplace=True)
        
        print(f"Filled missing values using '{fill_strategy}' strategy.")
    
    print(f"Data cleaning complete. Final shape: {cleaned_df.shape}")
    return cleaned_df

def validate_data(df, required_columns=None, min_rows=1):
    """
    Validate the DataFrame structure and content.
    
    Parameters:
    df (pd.DataFrame): DataFrame to validate.
    required_columns (list): List of column names that must be present.
    min_rows (int): Minimum number of rows required.
    
    Returns:
    bool: True if validation passes, False otherwise.
    """
    if required_columns:
        missing_cols = [col for col in required_columns if col not in df.columns]
        if missing_cols:
            print(f"Missing required columns: {missing_cols}")
            return False
    
    if df.shape[0] < min_rows:
        print(f"DataFrame has only {df.shape[0]} rows, minimum required is {min_rows}.")
        return False
    
    print("Data validation passed.")
    return True

