def clean_dataset(df, missing_strategy='mean', outlier_method='iqr'):
    """
    Clean a pandas DataFrame by handling missing values and outliers.
    
    Args:
        df (pd.DataFrame): Input DataFrame to clean
        missing_strategy (str): Strategy for handling missing values.
                               Options: 'mean', 'median', 'mode', 'drop'
        outlier_method (str): Method for detecting outliers.
                             Options: 'iqr', 'zscore'
    
    Returns:
        pd.DataFrame: Cleaned DataFrame
    """
    cleaned_df = df.copy()
    
    # Handle missing values
    if missing_strategy == 'mean':
        cleaned_df = cleaned_df.fillna(cleaned_df.mean())
    elif missing_strategy == 'median':
        cleaned_df = cleaned_df.fillna(cleaned_df.median())
    elif missing_strategy == 'mode':
        cleaned_df = cleaned_df.fillna(cleaned_df.mode().iloc[0])
    elif missing_strategy == 'drop':
        cleaned_df = cleaned_df.dropna()
    
    # Handle outliers for numeric columns only
    numeric_cols = cleaned_df.select_dtypes(include=[np.number]).columns
    
    for col in numeric_cols:
        if outlier_method == 'iqr':
            Q1 = cleaned_df[col].quantile(0.25)
            Q3 = cleaned_df[col].quantile(0.75)
            IQR = Q3 - Q1
            lower_bound = Q1 - 1.5 * IQR
            upper_bound = Q3 + 1.5 * IQR
            
            # Cap outliers
            cleaned_df[col] = cleaned_df[col].clip(lower=lower_bound, upper=upper_bound)
            
        elif outlier_method == 'zscore':
            mean_val = cleaned_df[col].mean()
            std_val = cleaned_df[col].std()
            
            # Calculate z-scores
            z_scores = np.abs((cleaned_df[col] - mean_val) / std_val)
            
            # Cap values with z-score > 3
            mask = z_scores > 3
            if mask.any():
                # Replace outliers with nearest non-outlier bounds
                non_outliers = cleaned_df[col][~mask]
                if len(non_outliers) > 0:
                    lower_bound = non_outliers.min()
                    upper_bound = non_outliers.max()
                    cleaned_df.loc[mask, col] = cleaned_df.loc[mask, col].clip(
                        lower=lower_bound, upper=upper_bound
                    )
    
    return cleaned_df

