def remove_outliers_iqr(df, columns):
    df_clean = df.copy()
    for col in columns:
        Q1 = df_clean[col].quantile(0.25)
        Q3 = df_clean[col].quantile(0.75)
        IQR = Q3 - Q1
        lower_bound = Q1 - 1.5 * IQR
        upper_bound = Q3 + 1.5 * IQR
        df_clean = df_clean[(df_clean[col] >= lower_bound) & (df_clean[col] <= upper_bound)]
    return df_clean

def remove_outliers_zscore(df, columns, threshold=3):
    df_clean = df.copy()
    for col in columns:
        z_scores = np.abs(stats.zscore(df_clean[col].dropna()))
        df_clean = df_clean[(z_scores < threshold) | df_clean[col].isna()]
    return df_clean

def normalize_minmax(df, columns):
    df_norm = df.copy()
    for col in columns:
        min_val = df_norm[col].min()
        max_val = df_norm[col].max()
        if max_val > min_val:
            df_norm[col] = (df_norm[col] - min_val) / (max_val - min_val)
    return df_norm

def normalize_zscore(df, columns):
    df_norm = df.copy()
    for col in columns:
        mean_val = df_norm[col].mean()
        std_val = df_norm[col].std()
        if std_val > 0:
            df_norm[col] = (df_norm[col] - mean_val) / std_val
    return df_norm

def handle_missing_values(df, strategy='mean', columns=None):
    df_filled = df.copy()
    if columns is None:
        columns = df_filled.select_dtypes(include=[np.number]).columns
    
    for col in columns:
        if strategy == 'mean':
            fill_value = df_filled[col].mean()
        elif strategy == 'median':
            fill_value = df_filled[col].median()
        elif strategy == 'mode':
            fill_value = df_filled[col].mode()[0]
        elif strategy == 'constant':
            fill_value = 0
        else:
            continue
        
        df_filled[col].fillna(fill_value, inplace=True)
    
    return df_filled

def clean_dataset(df, numeric_columns, outlier_method='iqr', normalize_method='minmax', missing_strategy='mean'):
    df_cleaned = df.copy()
    
    if outlier_method == 'iqr':
        df_cleaned = remove_outliers_iqr(df_cleaned, numeric_columns)
    elif outlier_method == 'zscore':
        df_cleaned = remove_outliers_zscore(df_cleaned, numeric_columns)
    
    df_cleaned = handle_missing_values(df_cleaned, strategy=missing_strategy, columns=numeric_columns)
    
    if normalize_method == 'minmax':
        df_cleaned = normalize_minmax(df_cleaned, numeric_columns)
    elif normalize_method == 'zscore':
        df_cleaned = normalize_zscore(df_cleaned, numeric_columns)
    
    return df_cleaned

